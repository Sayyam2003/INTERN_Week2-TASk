# Store 3 coordinates (as a tuple)
coordinate = (10, 20, 30)

# Unpack the coordinates into separate variables
x, y, z = coordinate

# Print each value
print("x:", x)
print("y:", y)
print("z:", z)

#Swapping
# Initial values
a = 5
b = 10

# Swap using tuple assignment
a, b = b, a

# Print swapped values
print("a:", a)
print("b:", b)
